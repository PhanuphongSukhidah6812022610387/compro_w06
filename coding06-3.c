#include <stdio.h>

int main() {
    int x = 12, y = 7, z = 12;

    printf("+----+--------------------------------------------+---------+\n");
    printf("| No | Expression                                 | Result  |\n");
    printf("+----+--------------------------------------------+---------+\n");

    printf("| %-2d | x > y                                      | %-7d |\n", 1, x > y);      // x มากกว่า y
    printf("| %-2d | x < z                                      | %-7d |\n", 2, x < z);      // x น้อยกว่า z
    printf("| %-2d | x == z                                     | %-7d |\n", 3, x == z);     // x เท่ากับ z
    printf("| %-2d | x != y                                     | %-7d |\n", 4, x != y);     // x ไม่เท่ากับ y
    printf("| %-2d | (x %% 2 == 1) || (5 != (5/3))               | %-7d |\n", 5, (x % 2 == 1) || (5 != (5 / 3))); // x เป็นเลขคี่ หรือ 5 ไม่เท่ากับ 5/3
    printf("| %-2d | x < y                                      | %-7d |\n", 6, x < y);      // x น้อยกว่า y
    printf("| %-2d | (x + y) * 2                                | %-7d |\n", 7, (x + y) * 2); // ผลรวมของ x กับ y คูณ 2
    printf("| %-2d | (x %% 2 == 0) || (y %% 2 == 1)              | %-7d |\n", 8, (x % 2 == 0) || (y % 2 == 1)); // x เป็นเลขคู่ หรือ y เป็นเลขคี่
    printf("| %-2d | (x > y) && (x < y)                         | %-7d |\n", 9, (x > y) && (x < y)); // x มากกว่า y และ น้อยกว่า y (เป็นไปไม่ได้)

    printf("+----+--------------------------------------------+---------+\n");

    return 0;
}
